public class charAt {
    public static void main(String[] args) {
        String str = "Hello world!";
        System.out.println(str.charAt(0));// charAt(int index) is the method to get the character at a specific index
        System.out.println(str.charAt(1));
        System.out.println(str.charAt(2));
        char ch=str.charAt(6);//
        System.out.println(ch);
        System.out.println(str.length());// length() is the method to get the length of a string class



    }
    
}
